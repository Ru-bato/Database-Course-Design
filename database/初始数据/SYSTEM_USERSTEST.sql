create table USERSTEST
(
    USER_ID         VARCHAR2(50)  not null
        constraint PK_USERSTEST
            primary key,
    USERNAME        VARCHAR2(100) not null,
    PASSWORD        VARCHAR2(100) not null,
    IS_STUDENT      CHAR,
    STATUS          CHAR          not null,
    ID_NUMBER       VARCHAR2(18)
        constraint UNIQUE_ID_NUMBER
            unique,
    PHONE_NUMBER    VARCHAR2(100) not null
        constraint UNIQUE_PHONE_NUMBER
            unique,
    RIDING_INTERVAL VARCHAR2(20)
)
/

